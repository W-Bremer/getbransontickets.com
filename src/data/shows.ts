export function getShowBySlug(slug: string): Show | undefined {
  return shows.find((s) => s.slug === slug);
}

export function getShowsByCategory(category: string): Show[] {
  if (category === "all") return shows;
  return shows.filter((s) => s.category.includes(category));
}

export function getFeaturedShows(): Show[] {
  return shows.filter((s) => s.isFeatured);
}

export function getShowCategories(): string[] {
  const cats = new Set<string>();
  shows.forEach((s) => s.category.forEach((c) => cats.add(c)));
  return Array.from(cats);
}
